export function getPostBySlug(slug) {
  return posts.find((post) => post.slug === slug);
}

export function getAllPostSlugs() {
  return posts.map((post) => post.slug);
}

// Get related posts by category, excluding the current post
export function getRelatedPosts(currentSlug, category, limit = 3) {
  const sameCat = posts.filter(
    (p) => p.slug !== currentSlug && p.category === category
  );
  const others = posts.filter(
    (p) => p.slug !== currentSlug && p.category !== category
  );
  const result = [...sameCat, ...others].slice(0, limit);
  return result;
}

// Parse a post date string to ISO format for sitemap/schema
export function parsePostDate(dateStr) {
  const d = new Date(dateStr);
  return isNaN(d.getTime()) ? new Date().toISOString() : d.toISOString();
}

